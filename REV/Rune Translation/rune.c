// Each encoded byte is printed as two runic glyphs (high nibble then low nibble).
static const char* runeToken[16] = {
    "ᚠ", "ᚢ", "ᚦ", "ᚨ",
    "ᚱ", "ᚲ", "ᚷ", "ᚹ",
    "ᚺ", "ᚾ", "ᛁ", "ᛃ",
    "ᛇ", "ᛈ", "ᛉ", "ᛋ"
    };

static void initConsoleUtf8(void) {
    SetConsoleOutputCP(CP_UTF8);
    }

// Removes trailing CR/LF so the encoded compare uses exact input bytes.
static size_t trimLineEnd(char* s) {
    size_t n = 0u;
    if (s == NULL) {
        return 0u;
        }
    n = strlen(s);
    while (n > 0u && (s[n - 1u] == '\n' || s[n - 1u] == '\r')) {
        s[n - 1u] = '\0';
        n--;
        }
    return n;
    }

// Sanity check: runeMap must be bijective (no duplicate outputs).
static int validateMap(void) {
    uint8_t seen[256] = { 0 };
    size_t i = 0u;

    for (i = 0u; i < 256u; i++) {
        uint8_t out = runeMap[i];
        if (seen[out] != 0u) {
            return 0;
            }
        seen[out] = 1u;
        }
    return 1;
    }

// Rune-only renderer for all terminal output (messages, inscription, and flag).
static void printRunes(const uint8_t* data, size_t len, int addNewline) {
    size_t i = 0u;
    if (data == NULL) {
        return;
        }

    for (i = 0u; i < len; i++) {
        uint8_t v = data[i];
        fputs(runeToken[(v >> 4u) & 0x0Fu], stdout);
        fputs(runeToken[v & 0x0Fu], stdout);
        if (i + 1u < len) {
            fputc(' ', stdout);
            }
        }

    if (addNewline != 0) {
        fputc('\n', stdout);
        }
    }

int main(void) {
    char input[maxInputLen] = { 0 };
    uint8_t encoded[maxInputLen] = { 0 };
    size_t len = 0u;
    size_t i = 0u;

    initConsoleUtf8();

    if (validateMap() == 0) {
        return 1;
        }

    printRunes(msgIntroRunes, sizeof(msgIntroRunes), 1);
    printRunes(inscription, sizeof(inscription), 1);
    printRunes(msgPromptRunes, sizeof(msgPromptRunes), 0);
    fflush(stdout);

    if (fgets(input, (int)sizeof(input), stdin) == NULL) {
        return 1;
        }

    // Encode each input char with runeMap and compare against the embedded inscription bytes.
    len = trimLineEnd(input);
    if (len != sizeof(inscription)) {
        printRunes(msgFailRunes, sizeof(msgFailRunes), 1);
        return 0;
        }

    for (i = 0u; i < len; i++) {
        encoded[i] = runeMap[(uint8_t)input[i]];
        }

    if (memcmp(encoded, inscription, sizeof(inscription)) == 0) {
        printRunes(msgOkRunes, sizeof(msgOkRunes), 1);
        printRunes(flagRunes, sizeof(flagRunes), 1);
        }
    else {
        printRunes(msgFailRunes, sizeof(msgFailRunes), 1);
        }

    return 0;
    }
